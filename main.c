float target_position = 0;

//Pid parameters
float kp = 1.35;
float ki = 0.00005;
float kd = 0.01;
float control_signal = 0;

//PID-related
float previous_time;
float previous_error;
float error_integral;
float current_time;
float dtime;
float error_value;
float edot;







