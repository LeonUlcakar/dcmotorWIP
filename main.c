void calculate_PID()
{
  //Determining the elapsed time
  current_time = HAL_GetTick(); //current time
  dtime = (current_time - previous_time) / 1000000.0; //time difference in seconds
  previous_time = current_time; //save the current time for the next iteration to get the time difference
  //---
  error_value = motor_position - target_position; //Current position - target position (or setpoint)

  edot = (error_value - previous_error) / dtime; //edot = de/dt - kd term

  error_integral = error_integral + (error_value * dtime); //ki

  control_signal = (kp * error_value) + (kd * edot) + (ki * error_integral); //final sum

  previous_error = error_value; //save the error for the next iteration to get the difference (for edot)
}

void drive_motor()
{
  //Determine speed and direction based on the value of the control signal
  //direction
  if (control_signal < 0) //negative value: CCW
  {
	  motor_direction = -1;
  }
  else if (control_signal > 0) //positive: CW
  {
	  motor_direction = 1;
  }
  else
  {
	  motor_direction = 0;
  }
  //Speed
  PWM_value = (int)fabs(control_signal);
  if (PWM_value > 255)
  {
	  PWM_value = 255;
  }

  if (PWM_value < 30 && error_value != 0)
  {
	  PWM_value = 30; //npr
  }


  //set the direction
  if (motor_direction == -1) //-1 == CCW
  {
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_SPEED_LOW);
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7, GPIO_SPEED_HIGH);
  }
  else if (motor_direction == 1) // == 1, CW
  {
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_SPEED_HIGH);
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7, GPIO_SPEED_LOW);
  }
  else // == 0, stop/break
  {
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_SPEED_LOW);
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_7, GPIO_SPEED_LOW);
	  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_6, GPIO_SPEED_LOW);
	  PWM_value = 0;
    //set PWM to 0
  }
  //----------------------------------------------------
  //set the motor speed
  HAL_GPIO_WritePin(GPIOB, GPIO_PIN_5, PWM_value);
}

